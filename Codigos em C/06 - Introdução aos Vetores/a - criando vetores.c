#include <stdio.h>
#include <stdlib.h>

/*#########################################################################################################################################################
                                                     metodo para imprimir o vetor
  #########################################################################################################################################################*/
void imprimirVetor (int vetor[], int tam){
	
	int i = 0;
	printf("\n       [ ");
	
	while(i<tam-1){
		printf("%d, ", vetor[i]);
		i++;
	}
	printf("%d ] ", vetor[i]);
}

/*#########################################################################################################################################################
                                                     metodo para criar o vetor
  #########################################################################################################################################################*/
void criarVetor (int vetor[], int tam){
	
	
	int i = 0;
	
	
	while(i<tam){
		printf("   Digite um valor:  ");
		scanf("%d, ", &vetor[i]);
		i++;
	}
	
}



	
/*#########################################################################################################################################################
                                                     metodo para imprimir a logo da questao
  #########################################################################################################################################################*/	
void imprimirInicio(){
	
		printf("\n\n\n\n     ###########################################################################################################\n");
	printf("     ###########################################################################################################\n");
	printf("     ####                                                                                                    ###\n");
	printf("     ####                                                                                                    ###\n");
	printf("     ####                                         CRIANDO VETORES                                            ###\n");
	printf("     ####                                                                                                    ###\n");
	printf("     ####                                                                                                    ###\n");
	printf("     ###########################################################################################################\n");
	printf("     ###########################################################################################################\n\n\n\n");
	
}


/*#########################################################################################################################################################
                                                     metodo para iniciar o sistema
  #########################################################################################################################################################*/
int main(int argc, char *argv[]) {
	
	
	imprimirInicio();
	
	
	int tam = 10;
	int vetor[tam];
	
	printf("  Escolha o tamanho do vetor:   ");
	scanf("%d", &tam);
	
	printf("\n\n\n");
	criarVetor (vetor,tam);
	
	system("cls");
	imprimirInicio();
	printf("     O vetor criado com %d posicoes foi:\n\n       ", tam);
	imprimirVetor (vetor,tam);
	return 0;
}
